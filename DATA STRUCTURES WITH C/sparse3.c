//sparse addition
#include<stdio.h>
void main()
{
int i,j,m,n,k,p,q;
int a[10][3],b[10][3],c[10][3];
printf("enter no of rows and elements of a\n");
scanf("%d",&m);
for(i=0;i<m;i++)
for(j=0;j<3;j++)
scanf("%d",&a[i][j]);
printf("enter no of rows and elements of b");
scanf("%d",&n);
printf("enter b matrix\n");
for(i=0;i<n;i++)
for(j=0;j<3;j++)
scanf("%d",&b[i][j]);
for(i=0;i<m;i++)
{
for(j=0;j<3;j++)
{
printf("%5d",a[i][j]);
printf("\t");
}
printf("\n\n");
}
for(i=0;i<n;i++)
{
for(j=0;j<3;j++)
{
printf("%5d",b[i][j]);
printf("\t");
}
printf("\n");
}
p=a[0][2];
q=b[0][2];
i=0;j=0;k=0;
while((i<=p)&&(j<=q))
{
if(a[i][0]<b[j][0])
{
c[k][0]=b[i][0];
c[k][1]=b[i][1];
c[k][2]=b[i][2];
k++;
i++;
continue;
}
if(b[j][0]<a[i][0])
{
c[k][0]=a[j][0];
c[k][1]=a[j][1];
c[k][2]=a[j][2];
k++;
j++;
continue;
}
if(a[i][1]<b[j][1])
{
c[k][0]=a[i][0];
c[k][1]=a[i][1];
c[k][2]=a[i][2];
k++;
i++;
continue;
}
if(b[j][1]<a[i][1])
{
c[k][0]=b[j][0];
c[k][1]=b[j][1];
c[k][2]=b[j][2];
j++;
k++;
continue;
}
c[k][0]=a[i][0];
c[k][1]=a[i][1];
c[k][2]=a[i][2]+b[j][2];
i++;
j++;
k++;
}
while(i<=p)
{
c[k][0]=a[i][0];
c[k][1]=a[i][1];
c[k][2]=a[i][2];
k++;
i++;
}
while(j<=q)
{
c[k][0]=b[j][0];
c[k][1]=b[j][1];
c[k][2]=b[j][2];
k++;
j++;
}
c[0][2]=k-1;
printf("c[0][2]=%d",c[k][2]);
for(i=0;i<c[0][2];i++)
{
for(j=0;j<3;j++)
{
printf("%5d",c[i][j]);
printf("\n\n");
}
printf("\n");
}
}

